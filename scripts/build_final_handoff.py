    lines.append("4. **Review remaining sub-1.000 cells** in section 6 — most are 90%+ matches with small NaN-vs-NaN or borderline cases; spot-check `validation_report.csv` filtered by (trial, column).")
    lines.append("5. **Confirm 10013 / 14C0059G `Collection_Event_alt` bucketing** (Baseline / C2 / etc.) once sample manifests are available.")
    lines.append("")

    # ── Section 9: change log ──────────────────────────────────────────
    lines.append("## 9. Change log across the three review passes")
    lines.append("")
    lines.append("| Pass | Items resolved | Mismatch count |")
    lines.append("|---|---|---:|")
