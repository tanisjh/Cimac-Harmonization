    lines.append("|---|---|---|---|")
    lines.append("| S1400I | phase | A iff `cimac_part_id` starts with `CCZR`, B iff `CNKA` | Cross-tab: 54/54 A patients = CCZR prefix, 106/106 B = CNKA |")
