    lines.append("| P1 | 10104 os/pfs anchor, 10104 treatment, ABTC1603 clinical_benefit.binary, 10021 clinical_benefit.binary, E4412 os/pfs unit, E4412 treatment, GU16-257 clinical_benefit/phase/clinical_benefit.binary | 3,759 |")
