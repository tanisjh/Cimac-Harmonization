    lines.append("- Hypothesis: `1 iff (clinical_benefit.binary='R') OR (clinical_benefit.binary='SD' AND pfs_time>=183)` — match 86% / 78% on s1400i / EAY131_Z1D.")
    lines.append("- **NOT committed** per the no-silent-guessing policy.")
    lines.append("")
    lines.append("### pfs_bin (all 9 template trials)")
    lines.append("- Template does NOT track pfs_stat directly.")
    lines.append("- Hypothesis: `1 iff (pfs_time>=183) OR (pfs_stat=0)` — match 81% / 81% on s1400i / EAY131_Z1D.")
    lines.append("- **NOT committed** per policy.")
    lines.append("")
    lines.append("### S1400I `age`")
    lines.append("- Template age values are decimals (per-sample). Source `age_num` is integer enrollment age.")
    lines.append("- Per-sample collection date NOT present in any S1400I source file.")
    lines.append("- **Will stay NA** unless an external sample-collection-date file is supplied.")
    lines.append("")
    lines.append("### `Cimac.id` for 10013 and 14C0059G (new trials)")
    lines.append("- Exhaustive scan of every cell of every file in both trial dirs for any string starting with a known `cimac_part_id` and followed by a sample-suffix: **zero hits**.")
    lines.append("- External CIMAC sample manifest required.")
