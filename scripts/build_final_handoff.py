    lines.append("| GU16-257 | race | numeric codes: 1→white, 3→black, 5→asian, 99→unk | Template cross-tab (P2#4); NOT the CIDC standard |")
    lines.append("| ABTC1603 | clinical_benefit / clinical_benefit.binary | **Gold outputs:** derived from a 4-month / 120-day PFS cutoff (Dr. Ye, 2026-06-17): `pfs_time≥120 → Clin-Ben / R`, `pfs_time<120 → No C-B / NR`, regardless of death reason. ABTC1603 has no BOR/response source column. | Gold deliverables only (harmonized_9trials_gold.csv, harmonized_11trials.csv). pfs_time, pfs_stat, pfs_bin, bor_bin unchanged. The historical harmonized_9trials_reproduced.csv stays template-aligned (clinical_benefit NaN, clinical_benefit.binary constant `other`, all 148 rows; P1#3). |")
